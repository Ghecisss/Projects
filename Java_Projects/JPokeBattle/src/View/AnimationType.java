package View;

/**
 * enum per i tipi di animazione
 * @author Jacopo De Crescenzo
 * @version 1.0
 */
public enum AnimationType {
	/**
	 * entrata del pokemon del giocatore
 	 */
	ENTRATA_PLAYER,
	/**
	 * entrata del pokemon dell'avversario
	 */
	ENTRATA_AVVERSARIO,
	/**
	 * uscita del pokemon del giocatore
	 */
	USCITA_PLAYER,
	/**
	 * uscita del pokemon dell'avversario
	 */
	USCITA_AVVERSARIO,
	/**
	 * attacco di un pokemon
	 */
	ATTACCO,
	/**
	 * scrittura nel textbox
	 */
	TEXTBOX;
}
